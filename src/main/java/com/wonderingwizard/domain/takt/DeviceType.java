package com.wonderingwizard.domain.takt;

/**
 * Represents the types of container handling equipment (CHE) devices in the terminal.
 */
public enum DeviceType {
    /**
     * Quay Crane - handles container loading/unloading from vessels.
     */
    QC,

    /**
     * Terminal Truck - transports containers between different locations.
     */
    TT,

    /**
     * Rubber Tyred Gantry - handles container stacking in the yard.
     */
    RTG
}
